						<ol>
							<li>Navigate to <strong>Pricing Tables >> All Pricing Tables</strong> and click on <strong>Add New</strong> button to add new pricing table. Enter the pricing table name as well as corresponding pricing column features and click on <strong>Add Table</strong> button.</li>
							<li>Later, you can <strong>Edit</strong>, <strong>Delete</strong> and <strong>Reorder</strong> pricing column features at any time by clicking on the <strong>Edit Features</strong> link in the pricing table lists.</li>
							<li>Now, you will be able to add pricing columns. To add pricing column click on <strong>Add Columns</strong> link. Type all the necessary fields for your pricing column and after all done click on <strong>Add Package</strong> button. You can create pricing columns as many as possible. From advanced options you can set number of columns you want to show on each row, maximum 6 columns are recommended.</li>
							<li>If you want to show some info about the feature simply enter your info in the tooltip box under package features and it will be shown on pricing table while mouse over on the feature. To show the tick icon simply mark the checkbox and to show cross icon unmark the checkbox.</li>
							<li>Later, you can <strong>Edit</strong>, <strong>Delete</strong> and <strong>Reorder</strong> pricing columns and features at any time. For each pricing table you will get <strong>SHORTCODE</strong> with different pricing table ID, which will be generated automatically.</li>
							<li>Now, paste the shortcode in your posts or pages where you would like to show pricing table and publish. Visit the newly created post/page to see the magic!</li>
